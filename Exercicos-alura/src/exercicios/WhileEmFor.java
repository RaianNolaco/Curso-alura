package exercicios;

public class WhileEmFor {
	
	public static void main(String[] args) {

		System.out.println("\nContador com while");
		
		  int contador = 0;
	      while(contador <= 10){
	    	  
	            System.out.println(contador);
	            contador++;
	        }
	      
	      System.out.println("\nContador com For");
	      
	      for(int i = 0;i <= 10;i++){
	    	
	    	  System.out.println(i);
	    	  
	      }	
	}
}
